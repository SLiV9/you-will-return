use crate::wasm4::*;

pub fn draw(x: i32, y: i32)
{
	blit(
		&ROCKWALL_FROM_BOTTOM,
		x,
		y,
		ROCKWALL_FROM_BOTTOM_WIDTH,
		ROCKWALL_FROM_BOTTOM_HEIGHT,
		ROCKWALL_FROM_BOTTOM_FLAGS,
	);
}

// rockwall_from_bottom
const ROCKWALL_FROM_BOTTOM_WIDTH: u32 = 16;
const ROCKWALL_FROM_BOTTOM_HEIGHT: u32 = 20;
const ROCKWALL_FROM_BOTTOM_FLAGS: u32 = 0; // BLIT_1BPP
const ROCKWALL_FROM_BOTTOM: [u8; 40] = [
	0x61, 0xce, 0xf3, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
	0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
	0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
	0xff, 0xff, 0xff, 0xff,
];
