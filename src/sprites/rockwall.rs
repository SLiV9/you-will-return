use crate::wasm4::*;

pub fn draw(x: i32, y: i32)
{
	blit(
		&ROCKWALL,
		x,
		y,
		ROCKWALL_WIDTH,
		ROCKWALL_HEIGHT,
		ROCKWALL_FLAGS,
	);
}
// rockwall
const ROCKWALL_WIDTH: u32 = 16;
const ROCKWALL_HEIGHT: u32 = 20;
const ROCKWALL_FLAGS: u32 = 0; // BLIT_1BPP
const ROCKWALL: [u8; 40] = [
	0x73, 0x86, 0xff, 0xcf, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
	0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
	0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
	0xff, 0xff, 0xff, 0xff,
];
